    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
         try {
            String ID=txtusername.getText();
            String passwd=new String(txtpass.getPassword());
        if(ID.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "You forgot to put username?");
            txtusername.requestFocus();
            return;
        }
        if(passwd.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "You forgot to put password?");
            txtpass.requestFocus();
            return;
        }
        if(!LoginDAO.checkLogin(ID, passwd))
        {
            JOptionPane.showMessageDialog(null, "Your account is not found.");
            txtusername.requestFocus();
            return;
        }else
        {
          ArmorClient am=new ArmorClient();
          am.setVisible(true);
          this.dispose();
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

